def seed():
    db = SessionLocal()
    try:
        existing = db.query(County).count()
        if existing >= 58:
            print(f"Counties already seeded ({existing} rows). Skipping.")
            return

        for code, name, fips, lat, lng in COUNTIES:
            db.merge(County(code=code, name=name, fips=fips, latitude=lat, longitude=lng))
        db.commit()
        print(f"Seeded {len(COUNTIES)} California counties.")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
